/**
 * -----------------------------------------------------------------------------
 * @brief			TFX100 Terrain follower main application screen
 *-----------------------------------------------------------------------------
 * @param app		Pointer to TFX100 application structure
 * -----------------------------------------------------------------------------
 */
void GUI_TFX100MainScreen(GUI_TFX100App_t *ui)
{
	// Exit if state is inactive
	if (ui->priv.status == GUI_STATUS_DISABLED)
		return;

	// If there was a jump from a previous menu,
	// perform the keyboard reading in the next cycle
	if (ui->priv.status == GUI_STATUS_JUMP)
	{
		ui->priv.status = GUI_STATUS_ENABLED;
		return;
	}

	// Initialization
	if (ui->priv.init == false)
		ui_Init(ui);

	// Process keyboard actions
	if (ui_KeyActions(ui))
		return;

	// Draw the menu on screen
	if (ui->priv.redraw)
		ui_Draw(ui);
}

/**
 * ----------------------------------------------------------------------------
 * @brief 			Screen initialization
 * ----------------------------------------------------------------------------
 * @param app		Pointer to TFX100App type structure
 * ----------------------------------------------------------------------------
 */
void ui_Init(GUI_TFX100App_t *ui)
{
	ui->priv.init = true;
	ui->priv.redraw = true;
}

/**
 * ----------------------------------------------------------------------------
 * @brief 			Perform keyboard actions
 * ----------------------------------------------------------------------------
 * @param app		Pointer to FTX100App type structure
 * @return			False = Continue, True = Exit menu.
 * ----------------------------------------------------------------------------
 */
bool ui_KeyActions(GUI_TFX100App_t *ui)
{
	// Escape action
	if (KEYBOARD_Event.Esc == 1)
	{
		GUI_TransferControl(&ui->priv.status, ui->pRet);
		ui->priv.redraw = true;
		return true;
	}

//	// Go to debug screen
//	if (KEYBOARD_Event.N0 > 5 && app->pDebugScreen != NULL)
//	{
//		GUI_TransferControl(&app->priv.status, app->pDebugScreen);
//		app->priv.redraw = true;
//		return true;
//	}

	// Enable dosing system
	if (KEYBOARD_Event.Pwr == 1)
		*ui->pAutoMode = !(*ui->pAutoMode);

	// Reset arms action
	*ui->pLeftArm = APP_ARM_HOLD;
	*ui->pRightArm = APP_ARM_HOLD;

	// Keyboard N1
	if (KEYBOARD_Event.N1 >= 1)
		*ui->pLeftArm = APP_ARM_UP;

	// Keyboard N2
	if (KEYBOARD_Event.N2_Up >= 1)
	{
		*ui->pLeftArm = APP_ARM_UP;
		*ui->pRightArm = APP_ARM_UP;
	}

	// Keyboard N3
	if (KEYBOARD_Event.N3 >= 1)
		*ui->pRightArm = APP_ARM_UP;

	// Keyboard N4
	if (KEYBOARD_Event.N4_Left >= 1)
		*ui->pLeftArm = APP_ARM_DOWN;

	// Keyboard N5
	if (KEYBOARD_Event.N5_Down >= 1)
	{
		*ui->pLeftArm = APP_ARM_DOWN;
		*ui->pRightArm = APP_ARM_DOWN;
	}

	// Keyboard N6
	if (KEYBOARD_Event.N6_Right >= 1)
		*ui->pRightArm = APP_ARM_DOWN;

//
//	// Enable simulation mode
//	if (KEYBOARD_Event.Bckspc == 5)
//		*app->pSimuMode = !(*app->pSimuMode);
//
//	if (KEYBOARD_Event.N7 == 1 || KEYBOARD_Event.N7 > 3)
//		if (*app->pSimuSpeed > 0)
//			*app->pSimuSpeed -= 5;
//
//	if (KEYBOARD_Event.N9 == 1 || KEYBOARD_Event.N9 > 3)
//		if (*app->pSimuSpeed < app->maxSpeed)
//			*app->pSimuSpeed += 5;

	ui->priv.redraw = true;
	return false;
}

/**
 * ----------------------------------------------------------------------------
 * @brief 			Draw screen
 * ----------------------------------------------------------------------------
 * @param app		Pointer to FTX100App type structure
 * ----------------------------------------------------------------------------
 */
void ui_Draw(GUI_TFX100App_t *ui)
{
	const uint8_t SENSOR_TEXT_YPOS = 25;
	const uint8_t SENSOR_LEFT_XPOS = 58;
	const uint8_t SENSOR_RIGHT_XPOS = 102;

	const uint8_t SENSOR_LEVEL_LEFT = 12;
	const uint8_t SENSOR_LEVEL_RIGHT = 106;

	const uint8_t SENSOR_LEVEL_MIN = 53;
	const uint8_t SENSOR_LEVEL_MAX = 13;

	const uint8_t LEFT_ARM_ARROW_X = 40;
	const uint8_t RIGHT_ARM_ARROW_X = 80;
	const uint8_t CENTER_WPOS_ICON_X = 60;
	const uint8_t BOTTOM_ICONS_Y = 53;

	uint8_t level = 0;
	char txtBox[10];

	// Draw screen
	GLCD_ScreenBitmap(TFX100App_Screen_128x64);
	ui->priv.redraw = false;

	SYSTEM_DelayUs(5);

	// ------------------------------------------------------------------------
	// Text boxes
	// ------------------------------------------------------------------------

	// Update Left Angle Sensor
	// ------------------------------------------------------------------------
	sprintf(txtBox, "%d", (int) *ui->pAngleSensor1);
	GLCD_DrawTextRight(txtBox, SENSOR_LEFT_XPOS, SENSOR_TEXT_YPOS, Font_NumArialBlack, 1, 1);

	// Update Right Angle Sensor
	// ------------------------------------------------------------------------
	sprintf(txtBox, "%d", (int) *ui->pAngleSensor2);
	GLCD_DrawTextRight(txtBox, SENSOR_RIGHT_XPOS, SENSOR_TEXT_YPOS, Font_NumArialBlack, 1, 1);

	// ------------------------------------------------------------------------
	// Icons
	// ------------------------------------------------------------------------

	// Left Arrow level indicator
	// ------------------------------------------------------------------------
	level = (uint8_t) ui_Map(*ui->pAngleSensor1, SENSOR_MIN_VALUE, SENSOR_MAX_VALUE, SENSOR_LEVEL_MIN, SENSOR_LEVEL_MAX);
	GLCD_PutBitmap(SENSOR_LEVEL_LEFT, level, TFX100_LeftArrowLevel, 0);

	// Right Arrow level indicator
	// ------------------------------------------------------------------------
	level = (uint8_t) ui_Map(*ui->pAngleSensor2, SENSOR_MIN_VALUE, SENSOR_MAX_VALUE, SENSOR_LEVEL_MIN, SENSOR_LEVEL_MAX);
	GLCD_PutBitmap(SENSOR_LEVEL_RIGHT, level, TFX100_RightArrowLevel, 0);

	// Left Arm action indicator
	// ------------------------------------------------------------------------
	switch (*ui->pLeftArm)
	{
	case APP_ARM_HOLD:
		break;

	case APP_ARM_UP:
		GLCD_PutBitmap(LEFT_ARM_ARROW_X, BOTTOM_ICONS_Y, TFX100_ArrowUp, 1);
		break;

	case APP_ARM_DOWN:
		GLCD_PutBitmap(LEFT_ARM_ARROW_X, BOTTOM_ICONS_Y, TFX100_ArrowDown, 1);
		break;
	}

	// Right Arm action indicator
	// ------------------------------------------------------------------------
	switch (*ui->pRightArm)
	{
	case APP_ARM_HOLD:
		break;

	case APP_ARM_UP:
		GLCD_PutBitmap(RIGHT_ARM_ARROW_X, BOTTOM_ICONS_Y, TFX100_ArrowUp, 1);
		break;

	case APP_ARM_DOWN:
		GLCD_PutBitmap(RIGHT_ARM_ARROW_X, BOTTOM_ICONS_Y, TFX100_ArrowDown, 1);
		break;
	}

	// Draw Working Position Icon
	// ------------------------------------------------------------------------
	if (*ui->pWorkingPosition)
		GLCD_PutBitmap(CENTER_WPOS_ICON_X, BOTTOM_ICONS_Y, TFX100_WorkingPos, 1);

	// Draw Auto/Manual Icon
	// ------------------------------------------------------------------------
//	if (*ui->pAutoMode)
//		GLCD_PutBitmap(43, 3, TFX100IconAuto, 0);
}

/**
 * ----------------------------------------------------------------------------
 * @brief
 * ----------------------------------------------------------------------------
 * @param x
 * @param in_min
 * @param in_max
 * @param out_min
 * @param out_max
 * @return
 * ----------------------------------------------------------------------------
 */
int ui_Map(int x, int in_min, int in_max, int out_min, int out_max)
{
	return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
}

