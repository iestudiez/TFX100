/**
 * -----------------------------------------------------------------------------
 * @brief			INX100 Fertilizer debug screen
 *-----------------------------------------------------------------------------
 * @param info		Pointer to INX100 debug screen structure
 * -----------------------------------------------------------------------------
 */
void GUI_TFX100DebugScreen(GUI_TFX100Debug_t *ui)
{
	// Exit if state is inactive
	if (ui->priv.status == GUI_STATUS_DISABLED)
		return;

	// If there was a jump from a previous menu,
	// perform the keyboard reading in the next cycle
	if (ui->priv.status == GUI_STATUS_JUMP)
	{
		ui->priv.status = GUI_STATUS_ENABLED;
		return;
	}

	// Initialization
	if (ui->priv.init == false)
		debugScreen_Init(ui);

	// Process keyboard actions
	if (debugScreen_KeyActions(ui))
		return;

	// Draw the menu on screen
	if (ui->priv.redraw)
		debugScreen_Draw(ui);
}

/**
 * ----------------------------------------------------------------------------
 * @brief 				INX100 calibration screen initialization
 * ----------------------------------------------------------------------------
 * @param info			Pointer to INX100Debug type structure
 * ----------------------------------------------------------------------------
 */
void debugScreen_Init(GUI_TFX100Debug_t *ui)
{
	ui->priv.init = true;
	ui->priv.redraw = true;
}

/**
 * ----------------------------------------------------------------------------
 * @brief 				Perform keyboard actions
 * ----------------------------------------------------------------------------
 * @param info			Pointer to INX100Debug type structure
 * @return				False = Continue, True = Exit current control.
 * ----------------------------------------------------------------------------
 */
bool debugScreen_KeyActions(GUI_TFX100Debug_t *ui)
{
	// Escape action
	if (KEYBOARD_Event.Esc == 1)
	{
		GUI_TransferControl(&ui->priv.status, ui->pRet);
		ui->priv.redraw = true;
		return true;
	}

	ui->priv.redraw = true;
	return false;
}

/**
 * ----------------------------------------------------------------------------
 * @brief 				Draw TFX100 Debug screen
 * ----------------------------------------------------------------------------
 * @param info			Pointer to TFX100Debug type structure
 * ----------------------------------------------------------------------------
 */
void debugScreen_Draw(GUI_TFX100Debug_t *ui)
{
	const uint8_t ROW1_Y = 10;
	const uint8_t ROW2_Y = 28;
	const uint8_t ROW3_Y = 46;

	const uint8_t COL1_X = 62;
	const uint8_t COL2_X = 98;

	char sTxtBox[10];
	uint8_t xPos;

	// Draw Debug Screen
	GLCD_ScreenBitmap(TFX100DebugScreen_128x64);
	SYSTEM_DelayUs(5);

	// Text boxes section
	// ========================================================================

	// Left sensor text box
	// ------------------------------------------------------------------------
	sprintf(sTxtBox, "%d", (int) PowerBoard.Ain[0]);
	xPos = COL1_X - GLCD_TextWidth(sTxtBox, Font_NumVerdanaBlack, 1);
	GLCD_DrawText(sTxtBox, xPos, ROW1_Y, Font_NumVerdanaBlack, 1, 1);

	// Right sensor text box
	// ------------------------------------------------------------------------
	sprintf(sTxtBox, "%d", (int) PowerBoard.Ain[1]);
	xPos = COL2_X - GLCD_TextWidth(sTxtBox, Font_NumVerdanaBlack, 1);
	GLCD_DrawText(sTxtBox, xPos, ROW1_Y, Font_NumVerdanaBlack, 1, 1);

	// Coil A1 text box
	// ------------------------------------------------------------------------
	sprintf(sTxtBox, "%d", (int) PowerBoard.Out[0].Current);
	xPos = COL1_X - GLCD_TextWidth(sTxtBox, Font_NumVerdanaBlack, 1);
	GLCD_DrawText(sTxtBox, xPos, ROW2_Y, Font_NumVerdanaBlack, 1, 1);

	// Coil B1 text box
	// ------------------------------------------------------------------------
	sprintf(sTxtBox, "%d", (int) PowerBoard.Out[2].DutyCycle);
	xPos = COL2_X - GLCD_TextWidth(sTxtBox, Font_NumVerdanaBlack, 1);
	GLCD_DrawText(sTxtBox, xPos, ROW2_Y, Font_NumVerdanaBlack, 1, 1);

	// Coil A2 text box
	// ------------------------------------------------------------------------
	sprintf(sTxtBox, "%d", (int) PowerBoard.Out[1].DutyCycle);
	xPos = COL1_X - GLCD_TextWidth(sTxtBox, Font_NumVerdanaBlack, 1);
	GLCD_DrawText(sTxtBox, xPos, ROW3_Y, Font_NumVerdanaBlack, 1, 1);

	// Coil B2 text box
	// ------------------------------------------------------------------------
	sprintf(sTxtBox, "%d", (int) PowerBoard.Out[3].DutyCycle);
	xPos = COL2_X - GLCD_TextWidth(sTxtBox, Font_NumVerdanaBlack, 1);
	GLCD_DrawText(sTxtBox, xPos, ROW3_Y, Font_NumVerdanaBlack, 1, 1);
}

